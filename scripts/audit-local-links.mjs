import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, relative, sep } from 'node:path';

const root = process.cwd();
const appDir = join(root, 'app');
const sourceDirs = ['app', 'components', 'lib'].map((dir) => join(root, dir)).filter(existsSync);
const ignoredPrefixes = ['/api/', '/portal/pay/success', '/portal/pay/cancel'];
const placeholderHrefs = new Set([
  '/assessments/update',
  '/unit-types/new',
  '/fixed-assets/new',
  '/forms/new',
  '/inspections/new',
  '/inventory/new',
  '/letters/new',
  '/projects/new',
  '/purchase-orders/new',
  '/recurring-work-orders/new',
  '/surveys/new',
  '/unit-turns/new',
  '/accounting-periods/close',
  '/vendor-receipts/new',
  '/other-receipts/new',
  '/subsidy-receipts/new',
  '/collections/signup',
  '/settings/resident-check-fees',
  '/gl-account-maps/new',
  '/journal-entries/post-gpr',
]);

function walk(dir, predicate = () => true) {
  const files = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) {
      if (entry.name === 'node_modules' || entry.name === '.next') continue;
      files.push(...walk(path, predicate));
    } else if (predicate(path)) {
      files.push(path);
    }
  }
  return files;
}

function routeFromPage(path) {
  let route = relative(appDir, path).split(sep).join('/');
  route = route.replace(/\/page\.tsx$/, '');
  route = route.replace(/(^|\/)\([^/]+\)/g, '');
  route = route.replace(/\[([^\]]+)\]/g, ':$1');
  route = '/' + route.replace(/^\/+/, '');
  return route === '/' ? '/' : route.replace(/\/$/, '');
}

const routePatterns = walk(appDir, (path) => path.endsWith('page.tsx')).map(routeFromPage);

function matchesRoute(href) {
  const path = href.split('?')[0].split('#')[0].replace(/\/$/, '') || '/';
  if (ignoredPrefixes.some((prefix) => path.startsWith(prefix))) return true;
  return routePatterns.some((pattern) => {
    const escaped = pattern
      .split('/')
      .map((part) => (part.startsWith(':') ? '[^/]+' : part.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')))
      .join('/');
    return new RegExp(`^${escaped}$`).test(path);
  });
}

const hrefRegex = /href=(?:\{`([^`]+)`\}|"([^"]+)"|'([^']+)')/g;
const missing = [];
const placeholders = [];

for (const file of sourceDirs.flatMap((dir) => walk(dir, (path) => /\.(tsx|ts)$/.test(path)))) {
  const text = readFileSync(file, 'utf8');
  let match;
  while ((match = hrefRegex.exec(text))) {
    const raw = match[1] ?? match[2] ?? match[3];
    if (!raw.startsWith('/')) continue;
    if (raw.includes('${')) continue;
    if (raw.startsWith('/help/') || raw === '#' || placeholderHrefs.has(raw)) {
      placeholders.push({ file, href: raw });
      continue;
    }
    if (!matchesRoute(raw)) missing.push({ file, href: raw });
  }
}

const lines = [
  '# Placeholder and Route Audit',
  '',
  'Generated by `npm run check:routes`.',
  '',
  '## Placeholder Links',
  '',
  ...(
    placeholders.length
      ? placeholders.map((item) => `- \`${relative(root, item.file)}\` -> \`${item.href}\``)
      : ['- None found.']
  ),
  '',
  '## Missing Local Routes',
  '',
  ...(
    missing.length
      ? missing.map((item) => `- \`${relative(root, item.file)}\` -> \`${item.href}\``)
      : ['- None found.']
  ),
  '',
];

writeFileSync(join(root, 'docs', 'placeholder-inventory.md'), lines.join('\n'));

if (missing.length) {
  console.error(`Found ${missing.length} local links without matching app routes. See docs/placeholder-inventory.md.`);
  process.exit(1);
}

console.log(`Route audit complete. ${placeholders.length} placeholders documented in docs/placeholder-inventory.md.`);
